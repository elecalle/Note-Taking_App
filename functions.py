#CREATE BUTTONS: fframe = fframeL / fframeR; command = button's function; text = button description
def create_buttons(fframe, fcommand, ftext, index):
    button = tk.Button(fframe, command = partial(fcommand, index), bd = 0, relief = "groove", compound = tk.CENTER, bg = "#BAD9D0", fg = "black",
                       activeforeground = "red", activebackground = "#BAD9D0", font = "Times 18", text = ftext,)
    return button

# CHECK THE INDEX FROM THE MAIN PROGRAM TO IDENTIFY THE CORRECT BUTTON
# ASSIGN THE FILE NAME TO A VARIABLE TO PROCEED WITH THE WRITING OPERATIONS
def compile(text_input, index):
    if index == 0:
        file_name = 'Probus.txt'
    elif index == 1:
        file_name = 'Sage.txt'
    elif index == 2:
        file_name = 'JALL.txt'
    elif index == 3:
        file_name = 'Heaviness.txt'
    elif index == 4: 
        file_name = 'ACoDe.txt'
    elif index == 5: 
        file_name = 'MISC.txt'   

    from datetime import date
    today = date.today()
    import time
    t = time.localtime()
    current_time = time.strftime("%H:%M:%S", t)
    w = str(today)
    fr = text_input[index].get("1.0","end")
    with open(file_name) as f:
        found = False
        for line in f:
            if w in line:
                l = open(file_name, 'a')
                l.write('\n')
                l.write(str(current_time) + '\n')
                l.write(str(fr))
                l.close()
                found = True
                text_input[index].delete("1.0","end")
                break
        if w not in line:
            l = open(file_name, 'a')
            l.write('\n----------------------------------------------- \n')
            l.write(str(today) + '\n')
            l.write(str(current_time) + '\n')
            l.write(str(fr))
            l.close()
            text_input[index].delete("1.0","end")